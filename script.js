function copiarCep() {
    if (!inputCep.value) {
        return;
    }
    inputCep.select();
    inputCep.setSelectionRange(0, 99999);
    navigator.clipboard.writeText(inputCep.value);
    const feedback = document.getElementById('feedback');
    feedback.style.display = 'block';
    setTimeout(() => feedback.style.display = 'none', 2000);
}